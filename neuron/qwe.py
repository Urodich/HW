def padding(input_images):
    
    a=[[[[0 for i in range(5)]for i in range(5)]for i in range(3)]for i in range(2)]
    a=torch.tensor(a)
    for img in range(2):
        for layer in range(3):
            for i in range(3):
                for j in range(3):
                    a[img][layer][i+1][j+1]=input_images[img][layer][i][j]
    return a.float()

def get_padding2d(input_images):
    padded_images = padding(input_images)
    return padded_images


